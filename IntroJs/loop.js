/**
 * For loop
 */
for(let i = 0; i <= 5; i++)
    {
        console.log(i);
    }
    
    /**
     * While loop
     */
    let j = 0;
    
    while(j<=5)
    {
        console.log(j);
        j++;
    }
    
    /**
     * Do While
     */
    let k = 0;
    
    do{
        console.log('Do While' + k);
        k++;
    }while(k <= 5);
    